@app.post('/users/', response_model=User)
async def create_user(user: UserIn):
    """Create/–î–æ–±–∞–≤–ª–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è"""
    # query = users.insert().values(name=user.name, email=user.email, lastname=user.lastname, passwd=user.passwd)
    query = users.insert().values(**user.model_dump())
    last_record_id = await database.execute(query)
    return {**user.model_dump(), 'id': last_record_id}


@app.get("/users/", response_model=List[User])
async def read_users():
    """Read/–ß—Ç–µ–Ω–∏–µ –≤—Å–µ—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π"""
    query = users.select()
    return await database.fetch_all(query)


@app.get("/users/{user_id}", response_model=User)
async def read_user(user_id: int):
    """Read/–ß—Ç–µ–Ω–∏–µ –æ–¥–Ω–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è"""
    query = users.select().where(users.c.id == user_id)
    return await database.fetch_one(query)


